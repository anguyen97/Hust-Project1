    //load data from arrayList to table
    private void showStudentList(ArrayList<SVTC> listSVTC) {
        DefaultTableModel model = (DefaultTableModel) table_SVTC.getModel();
        Object[] row;
        for (int i = 0; i < listSVTC.size(); i++) {
            row = new Object[9];
            row[0] = listSVTC.get(i).getId();
            row[1] = listSVTC.get(i).getPassword();
            row[2] = listSVTC.get(i).getName();
            row[3] = listSVTC.get(i).getBirthday();
            row[4] = listSVTC.get(i).getAddress();
            row[5] = listSVTC.get(i).getEmail();
            row[6] = listSVTC.get(i).getPhone();
            row[7] = listSVTC.get(i).getSchool();
            row[8] = listSVTC.get(i).getCourse();
            model.addRow(row);
        }
    }

