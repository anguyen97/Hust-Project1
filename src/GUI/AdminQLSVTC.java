    //click button delete > delete data in table and file
    private void btnDeleteSVTCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSVTCActionPerformed
        try {
            int i = table_SVTC.getSelectedRow();
            DefaultTableModel tableModel = (DefaultTableModel) table_SVTC.getModel();
            if (i >= 0) {
                //delete data in table
                String idDel = tableModel.getValueAt(i, 0).toString();
                tableModel.removeRow(i);
                //delete data in arraylist
                for (int j = 0; j < listSVTC.size(); j++) {
                    if (listSVTC.get(i).getId().equals(idDel)) {
                        listSVTC.remove(i);
                        JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        svtcApp.saveFile(listSVTC);
                        break;
                    }
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Xóa không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnDeleteSVTCActionPerformed

    //click button Edit > update data in table and file
    private void btnEditSVTCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditSVTCActionPerformed

        try {
            DefaultTableModel tableModel = (DefaultTableModel) table_SVTC.getModel();
            int i = table_SVTC.getSelectedRow();
            //edit table
            tableModel.setValueAt(textId.getText(), i, 0);
            tableModel.setValueAt(textPassword.getText(), i, 1);
            tableModel.setValueAt(textName.getText(), i, 2);
            tableModel.setValueAt(textBirthday.getText(), i, 3);
            tableModel.setValueAt(textAddress.getText(), i, 4);
            tableModel.setValueAt(textEmail.getText(), i, 5);
            tableModel.setValueAt(textPhone.getText(), i, 6);
            tableModel.setValueAt(selectSchool.getSelectedItem().toString(), i, 7);
            tableModel.setValueAt(selectCourse.getSelectedItem().toString(), i, 8);
            //edit arraylist
            listSVTC.get(i).setId(textId.getText());
            listSVTC.get(i).setPassword(textPassword.getText());
            listSVTC.get(i).setName(textName.getText());
            listSVTC.get(i).setBirthday(textBirthday.getText());
            listSVTC.get(i).setAddress(textAddress.getText());
            listSVTC.get(i).setEmail(textEmail.getText());
            listSVTC.get(i).setPhone(textPhone.getText());
            listSVTC.get(i).setSchool(selectSchool.getSelectedItem().toString());
            listSVTC.get(i).setCourse(selectCourse.getSelectedItem().toString());
            svtcApp.saveFile(listSVTC);
            JOptionPane.showMessageDialog(null, "Thao tác thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thao tác không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditSVTCActionPerformed

