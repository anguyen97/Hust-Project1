    //click button add a student
    private void btnAddADActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddADActionPerformed

        if (adminApp.searchAD(textId.getText(), textEmail.getText()) == -3 || adminApp.searchAD(textId.getText(), textEmail.getText()) == -1) {

            //add to arrayList
            Admin ad = new Admin();
            ad.setId(textId.getText());
            ad.setPassword(textPassword.getText());
            ad.setAddress(textAddress.getText());
            if (textName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tên không được để trống", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                ad.setName(textName.getText());
                if (checkData.checkBirthday(textBirthday.getText())) {
                    ad.setBirthday(textBirthday.getText());
                    if (checkData.checkEmail(textEmail.getText())) {
                        ad.setEmail(textEmail.getText());
                        if (checkData.checkPhone(textPhone.getText())) {
                            ad.setPhone(textPhone.getText());
                            listAD.add(ad);
                            adminApp.saveFile(listAD);
                            //add student's info to table 
                            DefaultTableModel tableModel = (DefaultTableModel) table_AD.getModel();
                            tableModel.addRow(new Object[]{textId.getText(), textPassword.getText(), textName.getText(),
                                textBirthday.getText(), textAddress.getText(), textEmail.getText(), textPhone.getText()});
                            JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "ID or email not available", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnAddADActionPerformed

