    //click button delete > delete data in table and file
    private void btnDeleteADActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteADActionPerformed
        // TODO add your handling code here:
        int i = table_AD.getSelectedRow();
        DefaultTableModel tableModel = (DefaultTableModel) table_AD.getModel();
        if (i >= 0) {
            //delete data in table
            String idDel = tableModel.getValueAt(i, 0).toString();
            tableModel.removeRow(i);
            //delete data in arraylist
            for (int j = 0; j < listAD.size(); j++) {
                if (listAD.get(i).getId().equals(idDel)) {
                    listAD.remove(i);
                    JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    adminApp.saveFile(listAD);
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnDeleteADActionPerformed

    //click button Edit > update data in table and file
    private void btnEditADActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditADActionPerformed

        try {
            DefaultTableModel tableModel = (DefaultTableModel) table_AD.getModel();

            int i = table_AD.getSelectedRow();
            //edit table
            tableModel.setValueAt(textId.getText(), i, 0);
            tableModel.setValueAt(textPassword.getText(), i, 1);
            tableModel.setValueAt(textName.getText(), i, 2);
            tableModel.setValueAt(textBirthday.getText(), i, 3);
            tableModel.setValueAt(textAddress.getText(), i, 4);
            tableModel.setValueAt(textEmail.getText(), i, 5);
            tableModel.setValueAt(textPhone.getText(), i, 6);

            //edit arraylist
            listAD.get(i).setId(textId.getText());
            listAD.get(i).setPassword(textPassword.getText());
            listAD.get(i).setName(textName.getText());
            listAD.get(i).setBirthday(textBirthday.getText());
            listAD.get(i).setAddress(textAddress.getText());
            listAD.get(i).setEmail(textEmail.getText());
            listAD.get(i).setPhone(textPhone.getText());
            adminApp.saveFile(listAD);
            JOptionPane.showMessageDialog(null, "Thao tác thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thao tác không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditADActionPerformed

