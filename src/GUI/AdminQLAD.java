    //load data from arrayList to table
    private void showAdminList(ArrayList<Admin> listAD) {
        DefaultTableModel model = (DefaultTableModel) table_AD.getModel();
        Object[] row;
        for (int i = 0; i < listAD.size(); i++) {
            row = new Object[7];
            row[0] = listAD.get(i).getId();
            row[1] = listAD.get(i).getPassword();
            row[2] = listAD.get(i).getName();
            row[3] = listAD.get(i).getBirthday();
            row[4] = listAD.get(i).getAddress();
            row[5] = listAD.get(i).getEmail();
            row[6] = listAD.get(i).getPhone();
            model.addRow(row);
        }
    }

