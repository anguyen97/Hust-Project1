    //load data from arrayList to table
    private void showLecturerList(ArrayList<Lecturer> listLecturer) {
        DefaultTableModel model = (DefaultTableModel) table_GV.getModel();
        Object[] row;
        for (int i = 0; i < listLecturer.size(); i++) {
            row = new Object[9];
            row[0] = listLecturer.get(i).getId();
            row[1] = listLecturer.get(i).getPassword();
            row[2] = listLecturer.get(i).getName();
            row[3] = listLecturer.get(i).getBirthday();
            row[4] = listLecturer.get(i).getAddress();
            row[5] = listLecturer.get(i).getEmail();
            row[6] = listLecturer.get(i).getPhone();
            row[8] = listLecturer.get(i).getSchool();
            row[7] = listLecturer.get(i).getLiteracy();
            model.addRow(row);
        }
    }

