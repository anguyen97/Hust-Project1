    //click button delete > delete data in table and file
    private void btnDeleteGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteGVActionPerformed
        // TODO add your handling code here:
        try {
            int i = table_GV.getSelectedRow();
            DefaultTableModel tableModel = (DefaultTableModel) table_GV.getModel();
            if (i >= 0) {
                //delete data in table
                String idDel = tableModel.getValueAt(i, 0).toString();
                tableModel.removeRow(i);
                //delete data in arraylist
                for (int j = 0; j < listLecturer.size(); j++) {
                    if (listLecturer.get(i).getId().equals(idDel)) {
                        listLecturer.remove(i);
                        JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        lecturerApp.saveFile(listLecturer);
                        break;
                    }
                }
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Xóa không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteGVActionPerformed

    //click button Edit > update data in table and file
    private void btnEditQLGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQLGVActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) table_GV.getModel();
        try {
            int i = table_GV.getSelectedRow();
            //edit table
            tableModel.setValueAt(textId.getText(), i, 0);
            tableModel.setValueAt(textPassword.getText(), i, 1);
            tableModel.setValueAt(textName.getText(), i, 2);
            tableModel.setValueAt(textBirthday.getText(), i, 3);
            tableModel.setValueAt(textAddress.getText(), i, 4);
            tableModel.setValueAt(textEmail.getText(), i, 5);
            tableModel.setValueAt(textPhone.getText(), i, 6);
            tableModel.setValueAt(textLiteracy.getText(), i, 7);
            tableModel.setValueAt(selectSchool.getSelectedItem().toString(), i, 8);

            //edit arraylist
            listLecturer.get(i).setId(textId.getText());
            listLecturer.get(i).setPassword(textPassword.getText());
            listLecturer.get(i).setName(textName.getText());
            listLecturer.get(i).setBirthday(textBirthday.getText());
            listLecturer.get(i).setAddress(textAddress.getText());
            listLecturer.get(i).setEmail(textEmail.getText());
            listLecturer.get(i).setPhone(textPhone.getText());
            listLecturer.get(i).setLiteracy(textLiteracy.getText());
            listLecturer.get(i).setSchool(selectSchool.getSelectedItem().toString());

            lecturerApp.saveFile(listLecturer);
            JOptionPane.showMessageDialog(null, "Thao tác thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thao tác không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditQLGVActionPerformed

