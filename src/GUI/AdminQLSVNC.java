    //click button delete > delete data in table and file
    private void btnDeleteSVNCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSVNCActionPerformed
        // TODO add your handling code here:
        try {
            int i = table_SVNC.getSelectedRow();
            DefaultTableModel tableModel = (DefaultTableModel) table_SVNC.getModel();
            if (i >= 0) {
                //delete data in table
                String idDel = tableModel.getValueAt(i, 0).toString();
                tableModel.removeRow(i);
                //delete data in arraylist
                for (int j = 0; j < listSVNC.size(); j++) {
                    if (listSVNC.get(i).getId().equals(idDel)) {
                        listSVNC.remove(i);
                        JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        svncApp.saveFile(listSVNC);
                        break;
                    }
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Xóa không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteSVNCActionPerformed

    //click button Edit > update data in table and file
    private void btnEditQLSVNCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQLSVNCActionPerformed
        try {
            DefaultTableModel tableModel = (DefaultTableModel) table_SVNC.getModel();

            int i = table_SVNC.getSelectedRow();
            //edit table
            tableModel.setValueAt(textId.getText(), i, 0);
            tableModel.setValueAt(textPassword.getText(), i, 1);
            tableModel.setValueAt(textName.getText(), i, 2);
            tableModel.setValueAt(textBirthday.getText(), i, 3);
            tableModel.setValueAt(textAddress.getText(), i, 4);
            tableModel.setValueAt(textEmail.getText(), i, 5);
            tableModel.setValueAt(textPhone.getText(), i, 6);
            tableModel.setValueAt(selectSchool.getSelectedItem().toString(), i, 7);
            tableModel.setValueAt(selectCourse.getSelectedItem().toString(), i, 8);
            //edit arraylist
            listSVNC.get(i).setId(textId.getText());
            listSVNC.get(i).setPassword(textPassword.getText());
            listSVNC.get(i).setName(textName.getText());
            listSVNC.get(i).setBirthday(textBirthday.getText());
            listSVNC.get(i).setAddress(textAddress.getText());
            listSVNC.get(i).setEmail(textEmail.getText());
            listSVNC.get(i).setPhone(textPhone.getText());
            listSVNC.get(i).setSchool(selectSchool.getSelectedItem().toString());
            listSVNC.get(i).setCourse(selectCourse.getSelectedItem().toString());
            svncApp.saveFile(listSVNC);
            JOptionPane.showMessageDialog(null, "Thao tác thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thao tác không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditQLSVNCActionPerformed

