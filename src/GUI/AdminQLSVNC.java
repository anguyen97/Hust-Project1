            //add to arrayList
            SVNC svnc = new SVNC();
            svnc.setId(textId.getText());
            svnc.setPassword(textPassword.getText());
            svnc.setName(textName.getText());
            svnc.setBirthday(textBirthday.getText());
            svnc.setAddress(textAddress.getText());
            svnc.setEmail(textEmail.getText());
            svnc.setPhone(textPhone.getText());
            svnc.setSchool(selectSchool.getSelectedItem().toString());
