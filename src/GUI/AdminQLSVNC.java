    //load data from arrayList to table
    private void showStudentList(ArrayList<SVNC> listSVNC) {
        DefaultTableModel model = (DefaultTableModel) table_SVNC.getModel();
        Object[] row;
        for (int i = 0; i < listSVNC.size(); i++) {
            row = new Object[9];
            row[0] = listSVNC.get(i).getId();
            row[1] = listSVNC.get(i).getPassword();
            row[2] = listSVNC.get(i).getName();
            row[3] = listSVNC.get(i).getBirthday();
            row[4] = listSVNC.get(i).getAddress();
            row[5] = listSVNC.get(i).getEmail();
            row[6] = listSVNC.get(i).getPhone();
            row[7] = listSVNC.get(i).getSchool();
            row[8] = listSVNC.get(i).getCourse();
            model.addRow(row);
        }
    }

